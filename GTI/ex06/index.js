let numero1 = parseFloat(prompt('Digite um número:'))
let numero2 = parseFloat(prompt('Digite um outro número:'))

document.write(`Os números digitados foram ${numero1} e ${numero2}.<br>`)

document.write(`A soma dos números ${numero1} e ${numero2} é ${numero1 + numero2}.<br>`)

document.write(`A subtração dos números ${numero1} e ${numero2} é ${numero1 - numero2}.<br>`)

document.write(`A multiplicação dos números ${numero1} e ${numero2} é ${numero1 * numero2}.<br>`)

document.write(`A divisão dos números ${numero1} e ${numero2} é ${numero1 / numero2}.<br>`)

document.write(`A média dos números ${numero1} e ${numero2} é ${(numero1 + numero2) / 2}.<br>`)



